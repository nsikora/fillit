#include <stdlib.h>
#include "libft.h"

void	*ft_memalloc(size_t size)
{
	void	*mem;

	if ((mem = (void*)malloc(sizeof(*mem) * (size))) != NULL)
		ft_bzero(mem, size);
	return ((void*)mem);
}
