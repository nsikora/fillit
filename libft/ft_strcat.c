char	*ft_strcat(char *s1, const char *s2)
{
	int		i;
	int		j;

	j = 0;
	while (s1[j])
		j++;
	i = 0;
	while (s2[i])
	{
		s1[j] = s2[i];
		j++;
		i++;
	}
	s1[j] = '\0';
	return (s1);
}
